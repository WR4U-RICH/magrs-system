<h1>Participation Lifecycle</h1>

<p>
Organizations may participate in the Marshall AI Governance Readiness Standard (MAGRS) through a structured lifecycle designed to maintain ongoing awareness of artificial intelligence use within professional work.
</p>

<h2>Step 1 — Readiness Assessment</h2>

<p>
Organizations begin by completing the MAGRS AI Governance Readiness Assessment.  
The assessment encourages leadership to reflect on how artificial intelligence tools may already be influencing organizational workflows.
</p>

<h2>Step 2 — Participation Activation</h2>

<p>
Organizations that wish to participate formally may activate participation following completion of the assessment.
</p>

<p>
Activation fee: <strong>$85</strong>
</p>

<p>
Activation provides access to MAGRS governance materials and begins the participation process.
</p>

<h2>Step 3 — Participation Period</h2>

<p>
Annual participation in the MAGRS framework is <strong>$295 per year</strong>.
</p>

<p>
Participation periods begin on the first day of the next calendar quarter following activation.
</p>

<h2>Step 4 — Quarterly Readiness Reflection</h2>

<p>
Participating organizations submit a brief readiness reflection each quarter confirming continued awareness of AI use within the organization.
</p>

<p>
Reports are due at the end of each calendar quarter with a 10-day grace period.
</p>

<h2>Reporting Discipline</h2>

<p>
If a quarterly report is not submitted, participation credentials may be temporarily suspended until reporting is brought current.
</p>

<h2>Annual Renewal</h2>

<p>
Participation renews annually based on the organization’s participation anniversary.
</p>

<p>
Organizations maintaining participation for one full year advance to <strong>Level 2 — Continuing Organization</strong>.
</p>

