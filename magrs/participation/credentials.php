<h1>Participation Credentials</h1>

<p>
Organizations participating in the Marshall AI Governance Readiness Standard (MAGRS) may display participation credentials indicating their commitment to thoughtful governance of artificial intelligence within professional work.
</p>

<h2>Participation Badge</h2>

<p>
Participating organizations may display the MAGRS participation badge in appropriate contexts such as organizational websites, offices, and professional materials.
</p>

<p>
The badge signals that the organization has reflected on the responsibility implications of artificial intelligence-assisted work and maintains ongoing participation in the MAGRS framework.
</p>

<h2>Participation Certificate</h2>

<p>
Participating organizations may receive a framed certificate recognizing their participation in the MAGRS framework.
</p>

<p>
Certificates include the organization name, participation level, and organization identifier.
</p>

<h2>Participation Levels</h2>

<ul>
<li><strong>Level 1 — Participating Organization</strong></li>
<li><strong>Level 2 — Continuing Organization</strong></li>
</ul>

<p>
Organizations achieve Level 2 status after maintaining participation for one full year.
</p>

<h2>Verification</h2>

<p>
Organizations choosing to display MAGRS credentials may provide a verification link allowing clients or partners to confirm participation status.
</p>

<h2>Credential Integrity</h2>

<p>
MAGRS credentials may only be displayed by organizations with active participation status.
</p>

<p>
Credentials may be revoked or suspended if participation requirements are not maintained.
</p>

