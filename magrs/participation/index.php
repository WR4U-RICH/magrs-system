<h1>Participation in the MAGRS Framework</h1>

<p>
Organizations may participate in the Marshall AI Governance Readiness Standard (MAGRS) to demonstrate awareness of how artificial intelligence tools influence professional work.
</p>

<p>
Participation signals that an organization has considered the responsibility implications of AI-assisted work and has chosen to maintain ongoing awareness through periodic reflection.
</p>

<h2>Participation Model</h2>

<p>
Organizations begin by completing the AI Governance Readiness Assessment.
</p>

<p>
Organizations that wish to participate formally may activate participation and receive access to MAGRS governance materials and ongoing framework insights.
</p>

<h2>Participation Elements</h2>

<ul>
<li>AI Governance Readiness Assessment</li>
<li>Governance Starter Kit</li>
<li>Quarterly Governance Briefings</li>
<li>Quarterly Readiness Reflection</li>
<li>Participation Credentials</li>
</ul>

<h2>Participation Levels</h2>

<p>
MAGRS recognizes participation through a simple progression:
</p>

<ul>
<li><strong>Level 1 — Participating Organization</strong></li>
<li><strong>Level 2 — Continuing Organization</strong></li>
</ul>

<p>
Organizations achieve Level 2 status after maintaining participation for one full year.
</p>

