<h2>Purpose of the Standard</h2>

<p>
MAGRS encourages organizations to remain aware of how artificial intelligence tools influence their work and to maintain human responsibility for AI-assisted outcomes.
</p>

<blockquote>
Artificial intelligence may assist human decision-making, but responsibility always remains with humans.
</blockquote>

