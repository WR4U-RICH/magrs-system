<h1>Statement of Limitations</h1>

<p>
The Marshall AI Governance Readiness Standard (MAGRS) is a professional governance framework designed to encourage thoughtful reflection on the responsible use of artificial intelligence within organizations.
</p>

<h2>Guidance Framework</h2>

<p>
MAGRS provides guidance and governance awareness but does not regulate artificial intelligence technologies or organizational practices.
</p>

<p>
Participation in MAGRS indicates that an organization has considered the responsibility implications of AI-assisted work and has chosen to maintain ongoing awareness through periodic reflection.
</p>

<h2>No Legal Certification</h2>

<p>
MAGRS participation does not constitute legal certification, regulatory compliance, or professional accreditation.
</p>

<p>
Organizations remain fully responsible for their own decisions, policies, and actions related to the use of artificial intelligence technologies.
</p>

<h2>No Guarantee of Outcomes</h2>

<p>
MAGRS does not guarantee organizational outcomes, risk mitigation, or compliance with any laws or regulations.
</p>

<p>
The framework is intended to support thoughtful governance discussion rather than prescribe technical or legal standards.
</p>

<h2>Organizational Responsibility</h2>

<p>
All responsibility for the implementation and oversight of artificial intelligence within an organization remains with that organization’s leadership.
</p>

<h2>Framework Evolution</h2>

<p>
Artificial intelligence technologies are evolving rapidly. The MAGRS framework may evolve over time to reflect new developments and insights.
</p>

<p>
Participation in the framework does not imply endorsement by any government, regulatory body, or technology vendor.
</p>

